    Het doel van het spel is om een ‘Set’ van drie kaarten te identificeren uit 12 kaarten die op tafel liggen. Elke
    kaart heeft een variatie van de volgende vier kenmerken:
    <br>
    <br>
    KLEUR: Elke kaart is rood, groen of blauw.
    SYMBOLEN: Elke kaart bevat ovalen, kronkels (squiggles) of ruiten.
    AANTAL: Elke kaart heeft één, twee of drie symbolen.
    ARCEERSTIJL: Elke kaart is gevuld (solid), open of gestreept (striped).
    Een ‘Set’ bestaat uit drie kaarten waarbij elk kenmerk ofwel hetzelfde is op alle drie kaarten, ofwel verschillend
    is op alle drie kaarten.
    <br>
    <br>
    Met andere woorden: voor elk kenmerk in de ‘Set’ geldt dat het óf bij alle drie kaarten gelijk is, óf bij alle drie
    verschillend.
    <Br>
    <br>


    Het deck van het spel bestaat uit 81 kaarten. Elke kaart bestaat maar 1 keer. Dus de kaart: ovaal gevuld rood 3 zit
    maar 1 keer in het deck.
    <br>
    <br>
